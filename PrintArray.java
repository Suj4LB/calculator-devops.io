// Program to print an array
public class PrintArray {
    public static void main(String[] args) {
        // Define an array
        int[] array = {10, 20, 30, 40, 50};

        // Print the array
        System.out.println("Array elements are:");
        for (int element : array) {
            System.out.print(element + " ");
        }
    }
}
